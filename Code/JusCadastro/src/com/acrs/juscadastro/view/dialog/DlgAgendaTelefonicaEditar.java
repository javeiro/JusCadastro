    public void limpar() {
        pnlPessoal.limpar();
        pnlEndereco.limpar();
        pnlContato.limpar();
        pnlAdvogado.limpar();
        pnlNota.limpar();
    }

    private void habilitar() {
        pnlPessoal.habilitar();
        pnlEndereco.habilitar();
        pnlContato.habilitar();
        pnlAdvogado.habilitar();
        pnlNota.habilitar();
    }

    private boolean validado() {

        lblMensagem.setText(pnlPessoal.validarPreenchimentoNome());
        if (!lblMensagem.getText().equals("")) {
            return false;
        }

        lblMensagem.setText(pnlPessoal.validarUnicidadeNome());
        if (!lblMensagem.getText().equals("")) {
            return false;
        }

        lblMensagem.setText(pnlPessoal.validarPreenchimentoCPFJ());
        if (!lblMensagem.getText().equals("")) {
            return false;
        }

        lblMensagem.setText(pnlPessoal.validarUnicidadeCPFJ());
        if (!lblMensagem.getText().equals("")) {
            return false;
        }

        lblMensagem.setText(pnlPessoal.validarFormatoNascimento());
        if (!lblMensagem.getText().equals("")) {
            return false;
        }

        lblMensagem.setText(pnlAdvogado.validarPreenchimentoOAB());
        return lblMensagem.getText().equals("");
    }

    // Acesso a camada de controle ---------------------------------------------
    private void mostrarAgendaTelefonica(Integer id) {
        pnlPessoal.setIdAgendaTelefonica(id);
        try {
            ContatoControl.mostrarRegistro(this);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            dispose();
        }
    }
