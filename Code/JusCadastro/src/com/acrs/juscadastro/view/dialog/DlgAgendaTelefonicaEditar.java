    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (validado()) {
            boolean incluir = (pnlPessoal.getIdAgendaTelefonica() == null);
            try {
                if (pnlAdvogado.getChkAdvogado().isSelected()) {
                    AdvogadoControl.gravarRegistro(this);
                } else {
                    ContatoControl.gravarRegistro(this);
                }
                // Limpar formulario se estiver em modo de inclusao ------------
                if (incluir) {
                    JOptionPane.showMessageDialog(this, "Contato inclu√≠do com sucesso");
                    limpar();
                } else {
                    this.dispose();
                }
                // Foco para a primeira caixa de texto -------------------------
                pnlPessoal.getTxtNome().requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnOkActionPerformed

